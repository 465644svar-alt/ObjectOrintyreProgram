            print(f"üíª –ü–û–ë–ï–î–ò–¢–ï–õ–¨: {self.computer.name}! üíª")
            print(f"{self.computer.name} –≤—ã–∂–∏–ª —Å {self.computer.health} –∑–¥–æ—Ä–æ–≤—å—è!")
