import random
import time


class Hero:
    """
    –ö–ª–∞—Å—Å, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π –≥–µ—Ä–æ—è –≤ –∏–≥—Ä–µ
    """

    def __init__(self, name, health=100, attack_power=20):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫–ª–∞—Å—Å–∞ Hero
        name: –∏–º—è –≥–µ—Ä–æ—è
        health: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∑–¥–æ—Ä–æ–≤—å—è (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 100)
        attack_power: —Å–∏–ª–∞ –∞—Ç–∞–∫–∏ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 20)
        """
        self.name = name
        self.health = health
        self.attack_power = attack_power

    def attack(self, other):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è –∞—Ç–∞–∫–∏ –¥—Ä—É–≥–æ–≥–æ –≥–µ—Ä–æ—è
        other: –¥—Ä—É–≥–æ–π –≥–µ—Ä–æ–π (–æ–±—ä–µ–∫—Ç –∫–ª–∞—Å—Å–∞ Hero), –∫–æ—Ç–æ—Ä–æ–≥–æ –∞—Ç–∞–∫—É—é—Ç
        """
        # –í—ã—á–∏—Ç–∞–µ–º –∑–¥–æ—Ä–æ–≤—å–µ —É –∞—Ç–∞–∫—É–µ–º–æ–≥–æ –≥–µ—Ä–æ—è
        other.health -= self.attack_power
        # –í—ã–≤–æ–¥–∏–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∞—Ç–∞–∫–µ
        print(f"{self.name} –∞—Ç–∞–∫—É–µ—Ç {other.name} –∏ –Ω–∞–Ω–æ—Å–∏—Ç {self.attack_power} —É—Ä–æ–Ω–∞!")

    def is_alive(self):
        """
        –ú–µ—Ç–æ–¥ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, –∂–∏–≤ –ª–∏ –≥–µ—Ä–æ–π
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç True –µ—Å–ª–∏ –∑–¥–æ—Ä–æ–≤—å–µ > 0, –∏–Ω–∞—á–µ False
        """
        return self.health > 0

    def __str__(self):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –≥–µ—Ä–æ–µ
        """
        return f"{self.name}: –ó–¥–æ—Ä–æ–≤—å–µ = {self.health}, –ê—Ç–∞–∫–∞ = {self.attack_power}"


class Game:
    """
    –ö–ª–∞—Å—Å, —É–ø—Ä–∞–≤–ª—è—é—â–∏–π –∏–≥—Ä–æ–≤—ã–º –ø—Ä–æ—Ü–µ—Å—Å–æ–º
    """

    def __init__(self):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫–ª–∞—Å—Å–∞ Game
        –°–æ–∑–¥–∞–µ—Ç –≥–µ—Ä–æ–µ–≤ –¥–ª—è –∏–≥—Ä–æ–∫–∞ –∏ –∫–æ–º–ø—å—é—Ç–µ—Ä–∞
        """
        # –°–æ–∑–¥–∞–µ–º –≥–µ—Ä–æ—è –¥–ª—è –∏–≥—Ä–æ–∫–∞
        self.player = Hero("–ò–≥—Ä–æ–∫")
        # –°–æ–∑–¥–∞–µ–º –≥–µ—Ä–æ—è –¥–ª—è –∫–æ–º–ø—å—é—Ç–µ—Ä–∞ —Å –Ω–µ–º–Ω–æ–≥–æ —Å–ª—É—á–∞–π–Ω—ã–º–∏ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞–º–∏
        self.computer = Hero("–ö–æ–º–ø—å—é—Ç–µ—Ä",
                             health=random.randint(90, 110),
                             attack_power=random.randint(18, 22))

    def show_status(self):
        """
        –ú–µ—Ç–æ–¥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ —Å—Ç–∞—Ç—É—Å–∞ –≥–µ—Ä–æ–µ–≤
        """
        print("\n" + "=" * 40)
        print("–¢–ï–ö–£–©–ò–ô –°–¢–ê–¢–£–°:")
        print(self.player)
        print(self.computer)
        print("=" * 40 + "\n")

    def start(self):
        """
        –û—Å–Ω–æ–≤–Ω–æ–π –º–µ—Ç–æ–¥ –∏–≥—Ä—ã, –∫–æ—Ç–æ—Ä—ã–π –∑–∞–ø—É—Å–∫–∞–µ—Ç –∏–≥—Ä–æ–≤–æ–π —Ü–∏–∫–ª
        """
        print("üéÆ –î–û–ë–†–û –ü–û–ñ–ê–õ–û–í–ê–¢–¨ –í –¢–ï–ö–°–¢–û–í–´–ô –ë–û–ô! üéÆ")
        print("–ò–≥—Ä–æ–∫ –∏ –ö–æ–º–ø—å—é—Ç–µ—Ä —Å—Ä–∞–∂–∞—é—Ç—Å—è –¥–æ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –≤–∑–¥–æ—Ö–∞!")

        # –°—á–µ—Ç—á–∏–∫ —Ä–∞—É–Ω–¥–æ–≤
        round_number = 1

        # –ò–≥—Ä–æ–≤–æ–π —Ü–∏–∫–ª - –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è –ø–æ–∫–∞ –æ–±–∞ –≥–µ—Ä–æ—è –∂–∏–≤—ã
        while self.player.is_alive() and self.computer.is_alive():
            print(f"\n--- –†–ê–£–ù–î {round_number} ---")

            # –•–æ–¥ –∏–≥—Ä–æ–∫–∞
            if self.player.is_alive():
                print("\n>>> –•–û–î –ò–ì–†–û–ö–ê <<<")
