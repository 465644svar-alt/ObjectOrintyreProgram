# 3. –ü—Ä–æ—Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –º–µ—Ç–æ–¥—ã:
class Store:
    def __init__(self, name, address,items = None):
        self.name = name
        self.address = address
        self.items = items if items is not None else {}
    def add_product(self, item_name, price):
        self.items[item_name] = price
        print(f"–¢–æ–≤–∞—Ä: {item_name} –¥–æ–±–∞–≤–ª–µ–Ω –ø–æ —Ü–µ–Ω–µ {price}")
    def remove_product(self, item_name):
        if item_name in self.items:
            del self.items[item_name]
            print(f"–¢–æ–≤–∞—Ä {item_name} - –£–¥–∞–ª–µ–Ω")
        else:
            print(f"–¢–æ–≤–∞—Ä {item_name} -  –ù–µ –Ω–∞–π–¥–µ–Ω")

    def get_price(self, item_name):
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ü–µ–Ω—É —Ç–æ–≤–∞—Ä–∞ –∏–ª–∏ None, –µ—Å–ª–∏ —Ç–æ–≤–∞—Ä–∞ –Ω–µ—Ç."""
        return self.items.get(item_name, None)

    def update_price(self, item_name, new_price):
        """–û–±–Ω–æ–≤–ª—è–µ—Ç —Ü–µ–Ω—É —Ç–æ–≤–∞—Ä–∞, –µ—Å–ª–∏ –æ–Ω —Å—É—â–µ—Å—Ç–≤—É–µ—Ç."""
        if item_name in self.items:
            self.items[item_name] = new_price
            print(f"–¶–µ–Ω–∞ –Ω–∞ '{item_name}' –æ–±–Ω–æ–≤–ª–µ–Ω–∞ –¥–æ {new_price} —Ä—É–±.")
        else:
            print(f"–¢–æ–≤–∞—Ä '{item_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω, –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ –æ–±–Ω–æ–≤–∏—Ç—å —Ü–µ–Ω—É.")

    def show_products(self):
        """–í—ã–≤–æ–¥–∏—Ç —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö —Ç–æ–≤–∞—Ä–æ–≤ –∏ —Ü–µ–Ω."""
        if not self.items:
            print("–í –º–∞–≥–∞–∑–∏–Ω–µ –Ω–µ—Ç —Ç–æ–≤–∞—Ä–æ–≤.")
        else:
            print(f"\n–°–ø–∏—Å–æ–∫ —Ç–æ–≤–∞—Ä–æ–≤ –º–∞–≥–∞–∑–∏–Ω–∞ '{self.name}':")
            for name, price in self.items.items():
                print(f"- {name}: {price} —Ä—É–±.")
# üß™ –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
store1 = Store("–ß–µ—Ä–∫–∏–∑–∫–∞", "–õ–µ–Ω–∏–Ω–∞ 20", {"–∞—Ä–±—É–∑": 20})

